// A Java program for generating the first 20 ordinal numbers.  
public class OrdinalNumber  
{  
// main method  
public static void main(String argvs[])  
{  
// loop to iterate till 20 to generate  
// the first 20 ordinal numbers  
for(int i = 1; i <= 20; i++)  
{  
if(i == 1)  
{  
System.out.println("The " + i + "st ordinal number.");  
}  
else if(i == 2)  
{  
System.out.println("The " + i + "nd ordinal number.");  
}  
else if(i == 3)  
{  
System.out.println("The " + i + "rd ordinal number.");  
}  
else  
{  
System.out.println("The " + i + "th ordinal number.");  
}  
}  
}  
}  
